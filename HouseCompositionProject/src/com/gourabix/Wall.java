package com.gourabix;

public class Wall {

    private int length;
    private int height;
    private int width;

    public Wall(int length, int height, int width) {
        this.length = length;
        this.height = height;
        this.width = width;
    }

    public int getLength() {
        return length;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

}
