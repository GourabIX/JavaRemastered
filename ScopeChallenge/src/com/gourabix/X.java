package com.gourabix;

import java.util.Scanner;

public class X {
    private int x = -1;

    public X(Scanner x) {
        this.x = x.nextInt();
    }

    public void x() {
        for (int x = 1; x <= 12; x++) {
            System.out.println(this.x + " times " + x + " = " + this.x * x);
        }
    }
}
